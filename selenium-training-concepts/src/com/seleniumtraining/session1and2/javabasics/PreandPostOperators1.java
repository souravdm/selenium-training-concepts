package com.seleniumtraining.session1and2.javabasics;

// Operators
public class PreandPostOperators1 {

	public static void main(String[] args) {
		
		// > < == != >= <=
		
		int num = 3;
		int num2 = 3;
		
		System.out.println(num >= num2);
		
	
	// pre fix and post fix operator
		
		
		int num3 = 3;
		
		System.out.println(num3);
		
		System.out.println(num3++); // post fix operator
		
		System.out.println(num3);
		
		System.out.println(++num3); // pre fix operator
		
		System.out.println(num3);
			
		
	}

}
