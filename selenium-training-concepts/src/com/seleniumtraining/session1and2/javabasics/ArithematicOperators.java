package com.seleniumtraining.session1and2.javabasics;

public class ArithematicOperators {

	public static void main(String[] args) {
		// Arithmetic operators
		
		System.out.println("Devide : " + (5/2));
		System.out.println("Remainder : " + (5%2));
		System.out.println("Multiplication : " + (5*2));
		
		// logical
		int age = 20;
		
		if(age!=20){
			System.out.println("go to movie");
		}
		
		// pre and post fix operators
		
		int num = 1;
		
		System.out.println(++num);
		System.out.println(num);
		
		int num2 = 1;
		
		System.out.println(num2++);
		System.out.println(num2);
		
		// compound assignment
		num2 +=2;
		
		System.out.println(num2);
		
		
		
		
		
		
		
		
	}

}
