package com.seleniumtraining.session1and2.assignments;

public class CheckEvenOdd {

	public static void main(String[] args) {
		int OddEvenNum = 25;
		
		if (OddEvenNum % 2 == 0 ) {
				System.out.println("The Number - "+OddEvenNum+" is an even number");
		}
		else {
			System.out.println("The Number - "+OddEvenNum+" is an odd number");
		}
			
	}

}
